package learningjava;

public class ArithmeticOperator {

	public static void main(String[] args) {
		System.out.println(a1(10.5,20.5));
		System.out.println(b1(205.10,10.2));
		System.out.println(c1(20.5,10.6));
	}
		public static double a1(double d, double e)
		{
			return (d+e);
		}
		public static double b1(double d, double e)
		{
			return(d-e);
		}
		public static double c1(double d, double e)
		{
			return(d*e);
	    }
	
		}
